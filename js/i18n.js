let currentLang = 'es';
let typedInstance = null;

function getInitialLanguage() {
    const saved = localStorage.getItem('portfolio-lang');
    if (saved === 'es' || saved === 'en') {
        return saved;
    }
    const browserLang = (navigator.language || navigator.userLanguage || 'es').toLowerCase();
    return browserLang.startsWith('en') ? 'en' : 'es';
}

function updateTypedStrings(lang) {
    if (typedInstance) {
        typedInstance.destroy();
    }

    typedInstance = new Typed('.multiple-text', {
        strings: translations[lang].typed_strings,
        typeSpeed: 100,
        backSpeed: 100,
        backDelay: 1000,
        loop: true
    });
}

function updateSeoMeta(lang) {
    const title = translations[lang].page_title;
    const description = translations[lang].meta_description;

    document.title = title;

    const metaDescription = document.getElementById('meta-description');
    if (metaDescription) {
        metaDescription.setAttribute('content', description);
    }

    const titleMetaIds = ['og-title', 'twitter-title'];
    titleMetaIds.forEach((id) => {
        const element = document.getElementById(id);
        if (element) {
            element.setAttribute('content', title);
        }
    });

    const descriptionMetaIds = ['og-description', 'twitter-description'];
    descriptionMetaIds.forEach((id) => {
        const element = document.getElementById(id);
        if (element) {
            element.setAttribute('content', description);
        }
    });

    const ogLocale = document.getElementById('og-locale');
    if (ogLocale) {
        ogLocale.setAttribute('content', translations[lang].og_locale);
    }
}

function setLanguage(lang) {
    if (!translations[lang]) {
        return;
    }

    currentLang = lang;
    localStorage.setItem('portfolio-lang', lang);
    document.documentElement.lang = lang;
    updateSeoMeta(lang);

    document.querySelectorAll('[data-i18n]').forEach((el) => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key] !== undefined) {
            if (el.tagName === 'INPUT' && (el.type === 'submit' || el.type === 'button')) {
                el.value = translations[lang][key];
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });

    document.querySelectorAll('[data-i18n-html]').forEach((el) => {
        const key = el.getAttribute('data-i18n-html');
        if (translations[lang][key] !== undefined) {
            el.innerHTML = translations[lang][key];
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang][key] !== undefined) {
            el.placeholder = translations[lang][key];
        }
    });

    document.querySelectorAll('[data-i18n-alt]').forEach((el) => {
        const key = el.getAttribute('data-i18n-alt');
        if (translations[lang][key] !== undefined) {
            el.alt = translations[lang][key];
        }
    });

    const langSwitch = document.querySelector('.lang-switch');
    if (langSwitch) {
        langSwitch.setAttribute('aria-label', translations[lang].lang_switch_aria);
    }

    document.querySelectorAll('.lang-btn').forEach((btn) => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
        btn.setAttribute('aria-pressed', btn.dataset.lang === lang ? 'true' : 'false');
    });

    updateTypedStrings(lang);
}

function t(key) {
    return translations[currentLang][key] || '';
}

window.i18n = { setLanguage, t, getLang: () => currentLang };

document.addEventListener('DOMContentLoaded', () => {
    document.querySelectorAll('.lang-btn').forEach((btn) => {
        btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
    });

    setLanguage(getInitialLanguage());
});
